export function getPointById(id, points) {
  var index;
  for (index in points) {
    if (points[index].id == id.toString()){
      return points[index]
    }
  }
 }

export function getInfectedNumber(points) {
   var index, sum=0;
   for (index in points) {
     sum += points[index].infected
   }
   return sum;
 }

export function getCuredNumber(points) {
   var index, sum=0;
   for (index in points) {
     sum += points[index].cured
   }
   return sum;
 }

